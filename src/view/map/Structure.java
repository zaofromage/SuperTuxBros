package view.map;

import view.main.ViewObject;

import java.awt.*;

public class Structure extends ViewObject {

    private int x;
    private int y;
    private int width;
    private int height;

    public Structure(int id, int x, int y, int width, int height) {
        super(id);
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    @Override
    public void draw(Graphics g) {
        g.setColor(Color.RED);
        g.fillRect(x, y, width, height);
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

    public void set(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}